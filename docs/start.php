<h1>CodeIgniter Bookcase</h1>

<h2>What is it?</h2>

<p>Bookcase is just an extension of the <a href="http://www.codeigniter.com">CodeIgniter PHP Framework</a> that tries to focus - step by step - on creating SEO friendly websites.
Bookcase is a tool that tries to optimize the relation between the designer and (CodeIgniter) developer.</p>

<h2>How does it work?</h2>

<ol>
	<li>Foreach <b>template</b> (deliverd by designer/frontender), a <b>controller</b> get's (manualy) created by the developer.</li>
	<li>Specific <b>configuration</b> of each page, can be set in these controllers through <a href="?page=core">libraries</a>.</b></li>
	<li>The libraries will create predefined <b>databases</b> to automaticly store these configurations.</li>
	<li><b>CRUD functions</b> will be provided for each configuration, set in the controllers.</li>
</ol>

<h2>Is it a CMS?</h2>

<p>No, it's not. It can be used to easily create a CMS, but the focus is upon custom development.</p>