<h1>How to install Bookcase</h1>

<h2>Installation: step by step</h2>
<p>There is not much installation required for Bookcase. All the required settings will be pointed out by just visiting your fresh installation of Bookcase.</p>

<table class="table table-bordered">
	<tr>
		<td><strong>Step 1</strong></td>
		<td>Provide a base url</td>
		<td><code>/application/config/config.php</code></td>
	</tr>
	<tr class="warning">
		<td colspan="3">If you are not working in a subdirectory, skip the next step!</td>
	</tr>
		<td><strong>Step 2</strong></td>
		<td>Provide the subdirectory in the .htacess file</td>
		<td><code>/.htaccess</code></td>
	</tr>
	<tr>
		<td><strong>Step 3</strong></td>
		<td>Provide database settings</td>
		<td><code>/application/config/database.php</code></td>
	</tr>
</table>

<h2>Basic settings</h2>
<p>After installing step by step, you will see a message telling that Bookcase is installed! We now got an installation with the autoloaded <a href="?page=core">Core Library</a>, which provides us some basic features that most likely will come in very handy.</p>